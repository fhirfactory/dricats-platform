package net.fhirfactory.dricats.platform.middleware.jgroups.configuration;

import java.io.Serial;
import java.io.Serializable;

public class JChannelConfiguration implements Serializable {
    //
    // Housekeeping
    //
    @Serial
    private static final long serialVersionUID = 3403310129096773387L;

    //
    // Attributes
    //
    private String clusterName;
    private String channelName;
    private boolean isConfigFileBased;
    private String configFileName;

    //
    // Getters and Setters
    //

    public boolean isConfigFileBased() {
        return isConfigFileBased;
    }

    public void setConfigFileBased(boolean configFileBased) {
        isConfigFileBased = configFileBased;
    }

    public String getClusterName() {
        return clusterName;
    }

    public void setClusterName(String clusterName) {
        this.clusterName = clusterName;
    }

    public String getChannelName() {
        return channelName;
    }

    public void setChannelName(String channelName) {
        this.channelName = channelName;
    }

    public String getConfigFileName() {
        return configFileName;
    }

    public void setConfigFileName(String configFileName) {
        this.configFileName = configFileName;
    }
}
